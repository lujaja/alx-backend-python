#!/usr/bin/env python3
"""
Complex Types - list of floats
"""
from typing import List


def sum_list(input_list: List[float]) -> float:
    """
    Fucntion - sum_list

    Attributes:
        input_list (list): paramater

    Return:
        sum (float): The sum of the input_list
    """
    return (sum(input_list))
